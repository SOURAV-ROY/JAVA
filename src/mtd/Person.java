package mtd;

public class Person {
	
	String firstName;
	String lastName;
	int age;
	String city;
	String country;
	String occupation;
	
	public Person(String firstName, String lastName, int age, String city, String country, String occupation) {
		
		super();
		
		this.firstName = firstName;
		this.lastName = lastName;
		this.age = age;
		this.city = city;
		this.country = country;
		this.occupation = occupation;
	}

}
